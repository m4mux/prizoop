bool getCGBTableEntry(unsigned char* romInternalName, unsigned short* allPalettes) {
	// compute rom name hash
	unsigned char hash = 0;
	for (int i = 0x00; i < 0x10; i++) {
		hash += romInternalName[i];
	}
	unsigned char disamb = romInternalName[3];

	int best = -1;
	for (unsigned int j = 0; j < sizeof(entries) / sizeof(entries[0]); j++) {
		if (entries[j].hash == hash) {
			if (entries[j].disamb == disamb) {
				// won't find a better one
				best = j;
				break;
			} else if (best == -1 || entries[j].disamb == 0) {
				best = j;
			}
		}
	}

	if (best != -1) {
		memcpy(allPalettes, entries[best].palette, sizeof(unsigned short) * 12);
		return true;
	}

	return false;
}